namespace Microsoft.Performance.SDK
{
    /// <summary>
    ///     Represents units of time.
    /// </summary>
    public enum TimeUnit
    {
        /// <summary>
        ///     Represents seconds.
        /// </summary>
        Seconds = 0,

        /// <summary>
        ///     Represents milliseconds.
        /// </summary>
        Milliseconds = 1,

        /// <summary>
        ///     Represents microseconds.
        /// </summary>
        Microseconds = 2,

        /// <summary>
        ///     Represents nanoseconds.
        /// </summary>
        Nanoseconds = 3
    }
}
